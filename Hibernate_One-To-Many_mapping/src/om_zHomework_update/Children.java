package om_zHomework_update;

public class Children {

	private int childrenId;
	private String childrenName;
	private int childrenAge;
	private String childrenClass;
	private int forevenId;

	public int getChildrenId() {
		return childrenId;
	}

	public void setChildrenId(int childrenId) {
		this.childrenId = childrenId;
	}

	public String getChildrenName() {
		return childrenName;
	}

	public void setChildrenName(String childrenName) {
		this.childrenName = childrenName;
	}

	public int getChildrenAge() {
		return childrenAge;
	}

	public void setChildrenAge(int childrenAge) {
		this.childrenAge = childrenAge;
	}

	public String getChildrenClass() {
		return childrenClass;
	}

	public void setChildrenClass(String childrenClass) {
		this.childrenClass = childrenClass;
	}

	public int getForevenId() {
		return forevenId;
	}

	public void setForevenId(int forevenId) {
		this.forevenId = forevenId;
	}

}
