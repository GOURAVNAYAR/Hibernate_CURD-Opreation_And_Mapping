package om_Delete;

public class Page {

	private int pageId;
	private String pageName;
	private String pageTopic;
	private int forevenId;

	public int getForevenId() {
		return forevenId;
	}

	public void setForevenId(int forevenId) {
		this.forevenId = forevenId;
	}

	public int getPageId() {
		return pageId;
	}

	public void setPageId(int pageId) {
		this.pageId = pageId;
	}

	public String getPageName() {
		return pageName;
	}

	public void setPageName(String pageName) {
		this.pageName = pageName;
	}

	public String getPageTopic() {
		return pageTopic;
	}

	public void setPageTopic(String pageTopic) {
		this.pageTopic = pageTopic;
	}

}
