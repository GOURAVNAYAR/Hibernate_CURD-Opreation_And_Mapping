package om_visual_save;

public class Part {

	private int partId;
	private String partName;
	private String partoftheCar;
	private int forevenId;

	public int getPartId() {
		return partId;
	}

	public void setPartId(int partId) {
		this.partId = partId;
	}

	public String getPartName() {
		return partName;
	}

	public void setPartName(String partName) {
		this.partName = partName;
	}

	public String getPartoftheCar() {
		return partoftheCar;
	}

	public void setPartoftheCar(String partoftheCar) {
		this.partoftheCar = partoftheCar;
	}

	public int getForevenId() {
		return forevenId;
	}

	public void setForevenId(int forevenId) {
		this.forevenId = forevenId;
	}

}
