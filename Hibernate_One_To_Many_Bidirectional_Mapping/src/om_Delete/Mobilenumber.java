package om_Delete;

public class Mobilenumber {
	private int mobilenumberId;
	private String mobilenumberName;
	private String mobileNumber;

	public int getMobilenumberId() {
		return mobilenumberId;
	}

	public void setMobilenumberId(int mobilenumberId) {
		this.mobilenumberId = mobilenumberId;
	}

	public String getMobilenumberName() {
		return mobilenumberName;
	}

	public void setMobilenumberName(String mobilenumberName) {
		this.mobilenumberName = mobilenumberName;
	}

	public String getMobileNumber() {
		return mobileNumber;
	}

	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}

}
