package mm_save;

public class Page {
	private int pageId;
	private String pageName;

	public int getPageId() {
		return pageId;
	}

	public void setPageId(int pageId) {
		pageId = pageId;
	}

	public String getPageName() {
		return pageName;
	}

	public void setPageName(String pageName) {
		pageName = pageName;
	}

}
